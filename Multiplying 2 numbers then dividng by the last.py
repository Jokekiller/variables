#Programme asking for three integers and multiplies two and divides by one

print("This program will ask for three integers and multiply two of them and then divides by the third")
number1 = int(input("Please give 1st number: "))
number2 = int(input("Please give 2nd number: "))
number3 = int(input("Please give 3rd number: "))
answer = number1 * number2 / number3
print("Answer is {0} * {1} / {2} = {3}".format(number1, number2, number3, answer))

              

#Fahrenheit to Centigrade converted

print("This proramme will convert Fahrenheit to Centigrade:")
fahrenheit = float(input("Give the Fahrenheit temperature:"))
centigrade = (fahrenheit - 32)*(5/9)
print("The temperature {0} in fahrenheit is {1} in centigrade".format(fahrenheit, centigrade))
